import numpy as np

# Заданный направляющий вектор
s = np.array([-3, 2, 1])

# Матрица проектирования на профильную плоскость (xy-плоскость)
P_xy = np.array([
    [1, 0, s[0] / s[2]],
    [0, 1, s[1] / s[2]],
    [0, 0, 0]
])

# Матрица проектирования на фронтальную плоскость (xz-плоскость)
P_xz = np.array([
    [1, s[0] / s[1], 0],
    [0, 0, 0],
    [0, s[2] / s[1], 1]
])

# Матрица проектирования на горизонтальную плоскость (yz-плоскость)
P_yz = np.array([
    [0, 0, 0],
    [s[1] / s[0], 1, s[2] / s[0]],
    [0, 0, 1]
])

# Вывод результатов
print("Матрица проектирования на профильную плоскость (xy):\n", P_xy)
print("Матрица проектирования на фронтальную плоскость (xz):\n", P_xz)
print("Матрица проектирования на горизонтальную плоскость (yz):\n", P_yz)
